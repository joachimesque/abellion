	<h1>Comment manger moins de viande</h1>

	<h2>Surveiller ses repas</h2>

	<p>
		Oui, c'est souvent très bon la viande quand on y est habitué. Mais il n'y a pas si longtemps, on n'en mangeait que rarement.
		On connaît mieux aujourd'hui les nutriments (protéines etc.) fournis par un repas végétarien, on peut donc espérer
		«&nbsp;bien vivre&nbsp;» même sans prendre de la viande à tous les repas.
	</p>

	<h2>Aller piocher chez les voisins</h2>

	<p>
		Voir du côté de la cuisine libanaise, thaï, indienne ou italienne, elles regorgent d’options
		végétariennes.
	</p>

	<h2>Envisager ses menus différemment</h2>

	<p>
		Si vous vous dites que manger végétarien, c'est triste, forcément ça ne donne pas envie.
		Représentez-vous une bonne salade d'été, avec des tomates, du concombre, de la feta.
		Ah tiens, pas de viande&nbsp;!
	</p>

	<p>
		Manger moins (ou ne plus manger du tout) de viande est plus facile si on ne le prend pas comme une corvée.
		Nous le disions ci-dessus&nbsp;: un mezze sans viande est plus qu'envisageable, un repas d'Inde du Sud est 
		succulent et pourtant végétarien. Faites appel à votre créativité ou à la bibliographie ci-dessous&nbsp;!
	</p>

