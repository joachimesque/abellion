	<h2>Ne pas se faire avoir par les marchands de doute (l’industrie agro-alimentaire)</h2>

	<p>
		Dans leur bouche, «&nbsp;Flexitarien&nbsp;» veut dire «&nbsp;qui mange surtout de la viande&nbsp;».
		Alors qu'il s'agit simplement d'un <a href="https://fr.wikipedia.org/wiki/Flexitarisme">omnivore qui mange seulement
		de la viande de manière occasionnelle</a> et pas systématique.
	</p>
